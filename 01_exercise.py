'''
    -- Retorna el tipo --
    En este desafío encontrarás una función llamada found_type que recibe un parámetro llamado value. 
    Debes encontrar el tipo de dato del parámetro valor y retornarlo desde la función found_type.
'''


def found_type(value):
    return type(value)


print(found_type(1))
print(found_type("Josue"))
print(found_type(True))
